window.onscroll = function () {myFunc()};
var mySticky = document.getElementById("mySticky");
var sticky = mySticky.offsetTop;
function myFunc() {
    if(window.pageYOffset >= sticky){
        mySticky.classList.add("sticky")
    }else {
        mySticky.classList.remove("sticky");
    }
}




